namespace LyiarOwl.AnimationPlayer
{
    public enum AnimationLoop
    {
        /// <summary>
        /// The animation will play just once.
        /// </summary>
        NoLoop,
        /// <summary>
        /// The animation will play repeating.
        /// </summary>
        Loop,
        /// <summary>
        /// The animation will play repeating, but going back and forth.
        /// </summary>
        PingPong
    }
}